const buildGeneratedProfile = (code: string, name: string, market?: string): StockProfile => {
  const industryProfile = isListingMarketOnly(market)
    ? inferIndustryProfileFromName(name)
    : getIndustryProfile(market) ?? inferIndustryProfileFromName(name);
  if (industryProfile) {
    return {
      description: `${name}${industryProfile.description}`,
      segments: industryProfile.segments,
      watchPoints: industryProfile.watchPoints,
      features: industryProfile.features,
    };
  }

  const theme = findNikkeiTheme(code);
  if (!theme) {
    return {
      description:
        `${name}は${market ? `${market}に属する` : "チャートページに追加された"}銘柄です。事業内容、決算、株価材料、セクター内での位置づけを、直近ニュースと出来高、チャートと合わせて確認します。`,
      segments: [market ?? "事業内容", "決算", "IR資料", "株価材料"],
      watchPoints: ["直近ニュース", "出来高", "チャート形状", "業績修正"],
      features: [market ?? "追加銘柄", "決算・IR", "出来高", "チャート"],
    };
  }

  const base = profileByTheme[theme];
  const specific = codeSpecificProfile[code];
  return {
    description:
      specific?.description ??
      `${name}は${theme}に分類される日経225採用銘柄です。${specific?.focus ?? base.business} ${base.drivers}`,
    segments: specific?.segments ?? base.segments,
    watchPoints: specific?.watchPoints ?? base.watchPoints,
    features: specific?.features ?? [theme, ...base.features.slice(0, 3)],
  };
};

