const buildGeneratedProfile = (code: string, name: string): StockProfile => {
  const theme = findNikkeiTheme(code);
  if (!theme) {
    return {
      description:
        `${name}はチャートページに追加された銘柄です。事業内容、決算、株価材料、セクター内での位置づけを、直近ニュースと出来高、チャートと合わせて確認します。`,
      segments: ["事業内容", "決算", "IR資料", "株価材料"],
      watchPoints: ["直近ニュース", "出来高", "チャート形状", "業績修正"],
      features: ["追加銘柄", "決算・IR", "出来高", "チャート"],
    };
  }

  const base = profileByTheme[theme];
  const specific = codeSpecificProfile[code];
  return {
    description:
      specific?.description ??
      `${name}は${theme}に分類される日経225採用銘柄です。${specific?.focus ?? base.business} ${base.drivers}`,
    segments: specific?.segments ?? base.segments,
    watchPoints: specific?.watchPoints ?? base.watchPoints,
    features: specific?.features ?? [theme, ...base.features.slice(0, 3)],
  };
};

